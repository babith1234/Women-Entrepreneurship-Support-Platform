import { pool } from "../config/db.js";
import { OrganisationSchema } from "../lib/schemas.js";

/**
 * @route GET /organisation
 * @description Gets all the organisation
 * @access public
 */
export async function getAllOrganisations(req, res) {
  try {
    const query = "SELECT * FROM ORGANISATION";
    const organisation = await pool.query(query);
    return res.status(200).json({ organisation: organisation.rows });
  } catch (error) {
    return res.status(500).json({ error: error.message });
  }
}

